function libTexAtlas.applyImage(widget, image)
	local initWidth		= widget:GetWidth()
	local initHeight	= widget:GetHeight()
	local coordWidth	= image.xPos[2] - image.xPos[1]
	local coordHeight	= image.yPos[2] - image.yPos[1]
	
	local widthPercent	= coordWidth / image.atlas.width
	local heightPercent	= coordHeight / image.atlas.height
	
	local partWidth		= initWidth * widthPercent
	local partHeight	= initHeight * heightPercent
	
	local scaleWidth	= initWidth * (initWidth / partWidth)
	local scaleHeight	= initHeight * (initHeight / partHeight)
	
	local xPosPercent	= image.xPos[1] / image.atlas.width
	local yPosPercent	= image.yPos[1] / image.atlas.height
	
	local offsetX		= (initWidth * xPosPercent)
	local offsetY		= (initHeight * yPosPercent)
	
	widget:SetUScale(scaleWidth..'p')
	widget:SetVScale(scaleHeight..'p')
	widget:SetUOffset(offsetX..'p')
	widget:SetVOffset(offsetY..'p')
end